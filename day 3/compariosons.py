print()

#equal to
print(" a == b:", a == b)

#not equal to
print(" a != b:", a != b)

#greater than
print(" a > b:", a > b)

#less than
print(" a < b:", a < b)

#greater than or equal to
print(" a >= b:", a >= b)

#less than or equal to
print(" a <= b:", a <= b)

