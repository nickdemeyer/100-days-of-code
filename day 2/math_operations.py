#math in python

a = 10
b = 3

print("a =", a)
print("b =", b)

#basic
print("addition:", a + b)
print("substraction:", a - b)
print("multiplication:", a * b)
print("division:", a / b)

#advanced
print("floor division:", a // b)
print("modulo (remainder):", a % b)
print("modulo:", a % b)
print("power:", a ** b)

#order of operations
result = 3 + 5 * 2
print("3 + 5 * 2 =", result)

result = (3 + 5) * 2
print("(3+5) * 2 =", result)