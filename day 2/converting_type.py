#converting text to numbers

#get input as text
text_number = input("enter a number: ")
print("as text: ", text_number, "- type: ", type(text_number))

#convert to integer
int_number = int(text_number)
print("as integer; ", int_number, "- type: ", type(int_number))

#convert to float
float_number = float(text_number)
print("as float: ", float_number, "- type: ", type(float_number))

#nu kunnen we math
doubled = int_number * 2
print("doubled: ", doubled)


