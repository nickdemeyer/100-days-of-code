# range() creates a sequence of numbers

# range(5) = 0, 1, 2, 3, 4
print("range(5):")
for i in range(5):
    print(i)
print()

#range(1,6) = 1, 2, 3, 4, 5
print("range(1,6):")
for i in range(1,6):
    print(i)

print()

#range (0,10,2) = 0, 2, 4, 6, 8 
print("range(0,10,2):")
for i in range(0,10,2):
    print(i)

print()

#you can count backwards
print("range(10,0,-1):")
for i in range(10,0,-1):
    print(i)